package uni.miskolc.ips.ilona.positioning.exceptions;

public class InvalidMeasurementException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4925340131364926237L;

	public InvalidMeasurementException() {
		super();
	}

	public InvalidMeasurementException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public InvalidMeasurementException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvalidMeasurementException(String arg0) {
		super(arg0);
	}

	public InvalidMeasurementException(Throwable arg0) {
		super(arg0);

	}
	
	

}
